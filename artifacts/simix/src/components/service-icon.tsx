/* ─── Resolve logo URL — convert dead clearbit URLs to Google favicon service ─── */
function resolveLogoUrl(url: string): string {
  const m = url.match(/logo\.clearbit\.com\/(.+)/);
  if (m) return `https://www.google.com/s2/favicons?domain=${m[1]!}&sz=128`;
  return url;
}

/* ─── Custom logo image (URL-based) ─── */
function CustomLogoIcon({ logoUrl, name, size, radius }: { logoUrl: string; name: string; size: number; radius: number }) {
  const src = resolveLogoUrl(logoUrl);
  return (
    <div
      className="flex items-center justify-center shrink-0 shadow-sm overflow-hidden bg-zinc-900"
      style={{ width: size, height: size, borderRadius: radius }}
    >
      <img
        src={src}
        alt={name}
        className="w-full h-full object-contain p-1"
        onError={(e) => {
          const el = e.currentTarget as HTMLImageElement;
          el.style.display = "none";
          const parent = el.parentElement;
          if (parent) {
            parent.style.background = "linear-gradient(135deg,#A855F7,#6366F1)";
            parent.innerHTML = `<span style="color:white;font-weight:700;font-size:${Math.round(size * 0.42)}px;user-select:none">${name.charAt(0).toUpperCase()}</span>`;
          }
        }}
      />
    </div>
  );
}

export function ServiceIcon({
  name,
  slug,
  logoUrl,
  size = 48,
  rounded = "xl",
}: {
  name: string;
  slug?: string;
  logoUrl?: string | null;
  size?: number;
  rounded?: "lg" | "xl" | "2xl" | "full";
}): ReactNode {
  const radius = { lg: 12, xl: Math.round(size * 0.26), "2xl": Math.round(size * 0.35), full: 9999 }[rounded];

  /* ── Custom logo from admin takes highest priority ── */
  if (logoUrl) {
    return <CustomLogoIcon logoUrl={logoUrl} name={name} size={size} radius={radius} />;
  }

  const k = keyFor(name, slug);
  const entry = k ? brands[k] : undefined;

  if (!entry) {
    return (
      <div
        className="flex items-center justify-center shrink-0 shadow-sm"
        style={{
          width: size, height: size, borderRadius: radius,
          background: "linear-gradient(135deg,#A855F7,#6366F1)",
        }}
      >
        <span className="text-white font-bold select-none" style={{ fontSize: size * 0.42 }}>
          {name.charAt(0).toUpperCase()}
        </span>
      </div>
    );
  }

  if (entry.type === "instagram") return <InstagramIcon size={size} />;
  if (entry.type === "google")    return <GoogleIcon size={size} radius={radius} />;
  if (entry.type === "microsoft") return <MicrosoftIcon size={size} radius={radius} />;
  if (entry.type === "tiktok")    return <TikTokIcon size={size} radius={radius} />;

  return (
    <div
      className="flex items-center justify-center shrink-0 shadow-sm"
      style={{ width: size, height: size, borderRadius: radius, background: entry.bg }}
    >
      <svg
        viewBox="0 0 24 24"
        width={size * 0.62}
        height={size * 0.62}
        fill={entry.fg}
        aria-label={name}
      >
        <path d={entry.path} />
      </svg>
    </div>
  );
}
